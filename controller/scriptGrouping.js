//문장 분리
function sentenceGrouping(inputtext) {

    //문장부호를 기준으로 문장 분리
    let sentences = inputtext.split(/([.?!,])/).filter(Boolean);

    // 문장 부호를 포함하여 다시 결합
    let groupedSentences = [];
    for (let i = 0; i < sentences.length; i += 2) {
        let sentence = sentences[i] + (sentences[i + 1] || ""); // 문장 부호 포함
        groupedSentences.push(sentence.trim());
    }
    return groupedSentences;
}

//타임 스탬프 추출
function gettimestamp(script) {
    let groupedSentences = sentenceGrouping(script.text);
    let timestamp = [];

    let wordindex = 0;

    let start = 0;
    let end = 0;
    groupedSentences.forEach((sentence) => {

        if (wordindex < script.words.length && script.words[wordindex].word == "▁") wordindex++; // 문장 양 끝 공백 제거

        let temp = "";
        start = script.words[wordindex].startTime;
        while (sentence.trim().length - 1 > temp.length) {//문장부호를 제외한 문장 길이로 일치 확인
            let word = script.words[wordindex].word;

            temp += word.replace("▁", " ");//파일로 저장된 json에서는 언더바가 해당 특수문자라 나중에 확인 필요
            wordindex++;
        }

        end = script.words[wordindex - 1].endTime;
        timestamp.push({sentence, start, end});

        if (wordindex < script.words.length && script.words[wordindex].word == "▁") wordindex++; // 문장 양 끝 공백 제거

    })
    return timestamp;
}

